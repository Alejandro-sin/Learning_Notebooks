"""
Este archivo tiene como propósito estudiar los ciclos FOR. sus variaciones, formas,

Un ciclo for para recorrer una estructura de datos cualquiera.


"""
# Ciclo para contar en un rango de números.

# Quiero que me imprima los primeros 1000 numeros.
for index in range(1001):
    print(index)

# Ciclo for para recorrer variables

phrase = "Alejandro"

# Esto me recorre el string y me devuelve en cada vuelta cada letra.
for word in phrase:
    print(word)


# Ciclo para recorrer una lista.

list_number = list(range(10))
print(list_number)
for number in list_number:
    print(number)


